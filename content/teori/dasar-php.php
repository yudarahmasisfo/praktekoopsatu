<h2>Materi Dasar PHP</h2>
<p>PHP (Hypertext Preprocessor) adalah bahasa scripting server-side yang sangat populer untuk pengembangan web. Dalam konteks Pemrograman Berorientasi Objek, PHP menyediakan fitur-fitur yang memungkinkan kita untuk menerapkan konsep OOP.</p>

<h3>Fitur OOP dalam PHP</h3>
<ul>
    <li>Class dan Object</li>
    <li>Encapsulation (Enkapsulasi)</li>
    <li>Inheritance (Pewarisan)</li>
    <li>Polymorphism (Polimorfisme)</li>
    <li>Abstraction (Abstraksi)</li>
</ul>

<div class="code-block">
<?php
// Contoh kode PHP sederhana
echo "Hello, World!";
?>
</div>

<p>PHP mendukung pemrograman prosedural dan berorientasi objek. Dalam pembelajaran ini, kita akan fokus pada pendekatan berorientasi objek.</p>

<div class="highlight">
    <p><strong>Info:</strong> Untuk menjalankan kode PHP, Anda memerlukan server web seperti Apache dengan PHP terinstal. Anda bisa menggunakan XAMPP, WAMP, atau MAMP untuk lingkungan pengembangan lokal.</p>
</div>

